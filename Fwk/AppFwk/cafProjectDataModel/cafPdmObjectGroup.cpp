PdmObjectGroup::~PdmObjectGroup()
{
}

//--------------------------------------------------------------------------------------------------
/// 
//--------------------------------------------------------------------------------------------------
void PdmObjectGroup::deleteObjects()
{
    size_t it;
    for (it = 0; it != objects.size(); ++it)
    {
        delete objects[it];
    }
}

//--------------------------------------------------------------------------------------------------
/// 
//--------------------------------------------------------------------------------------------------
void PdmObjectGroup::addObject(PdmObjectHandle * obj)
{
    objects.push_back(obj);
}



